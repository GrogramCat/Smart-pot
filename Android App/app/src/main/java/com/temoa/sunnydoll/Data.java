package com.temoa.sunnydoll;

public class Data {


    private String timestamp;
    private int value;

    public void setTimestamp(String timestamp) {
        this.timestamp = timestamp;
    }

    public void setValue(int value) {
        this.value = value;
    }

    public String getTimestamp() {
        return timestamp;
    }

    public int getValue() {
        return value;
    }
}


